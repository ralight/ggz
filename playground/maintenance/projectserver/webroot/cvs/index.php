Anonymous CVS checkouts can be done like thus:<br>
<i>cvs -d:pserver:anonymous@cvs.ggzgamingzone.org:/cvs -z3 co ...</i>
<br><br>

Developer CVS operations use SSH instead of pserver:<br>
<i>cvs -dusername@cvs.ggzgamingzone.org:/cvs commit ...</i>
<br><br>

CVS commit notifications are sent to the ggz-cvs mailing list.
